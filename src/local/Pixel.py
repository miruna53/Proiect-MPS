class Pixel:

    def __init__(self, pixel_value, pixel_class, thresholds):
        self.pixel_value = pixel_value
        self.pixel_class = pixel_class
        self.thresholds = thresholds

    def get_pixel_value(self):
        return self.pixel_value

    def set_pixel_value(self):
        return self.pixel_value

    def get_pixel_class(self):
        return self.pixel_class

    def set_pixel_class(self):
        return self.pixel_class

    def get_thresholds(self):
        return self.thresholds

    def set_thresholds(self):
        return self.thresholds
